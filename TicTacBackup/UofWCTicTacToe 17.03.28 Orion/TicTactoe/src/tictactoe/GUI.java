        int x = c.getX();
        int y = c.getY();
        
        String co = "" + x + "," + y;
        
        switch(co) {
            case "0,0": markO(zeroZero);
                        enableGrid();
                        break;
                        
            case "0,1": markO(zeroOne);
                        enableGrid();
                        break;
                        
            case "0,2": markO(zeroTwo);
                        enableGrid();
                        break;
                        
            case "1,0": markO(oneZero);
                        enableGrid();
                        break;
                        
            case "1,1": markO(oneOne);
                        enableGrid();
                        break;
                        
            case "1,2": markO(oneTwo);
                        enableGrid();
                        break;
                        
            case "2,0": markO(twoZero);
                        enableGrid();
                        break;
                        
            case "2,1": markO(twoOne);
                        enableGrid();
                        break;
                        
            case "2,2": markO(twoTwo);
                        enableGrid();
                        break;
                        
            default:    JOptionPane.showMessageDialog(null, "Error");
        }
        
